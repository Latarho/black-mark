type NineBoxCellDetail = {
  roleLabel: string
  perfLabel: string
  potLabel: string
  bucketIndex: number
}

function stableNumber(value: string): number {
  let hash = 0
  for (let i = 0; i < value.length; i++) hash = (hash * 31 + value.charCodeAt(i)) | 0
  return Math.abs(hash)
}

function staffToneClass(staffId: string): string {
  const hash = stableNumber(staffId)
  return STAFF_TONE_CLASSES[Math.abs(hash) % STAFF_TONE_CLASSES.length]
}

function getUnitLabel(unitId: string, unitOptions: UnitOption[]): string {
  if (unitId === "all") return "Все подразделения"
  return unitOptions.find((unit) => unit.id === unitId)?.path || unitId
}

function makeInitials(lastName: string, firstName: string, patronymic: string): string {
  const parts = [lastName, firstName, patronymic].filter(Boolean)
  return parts
    .map((part) => part.charAt(0).toUpperCase())
    .join("")
    .slice(0, 2)
}

function makeNineBoxBuckets(staff: StaffMember[]): StaffMember[][] {
  const buckets: StaffMember[][] = Array.from({ length: 9 }, () => [])
  staff.forEach((member, index) => {
    const value = `${member.id}|${member.lastName}|${member.firstName}|${member.patronymic}|${index}`
    const hash = stableNumber(value)
    const x = hash % 3
    const y = Math.floor((hash / 3) % 3)
    const indexBox = y * 3 + x
    buckets[indexBox].push(member)
  })
  return buckets
}

function MiniAvatar({ member }: { member: StaffMember }) {
  return (
    <span
      className={`flex size-10 shrink-0 items-center justify-center rounded-full text-base font-semibold ${staffToneClass(
        member.id
      )}`}
    >
      {makeInitials(member.lastName, member.firstName, member.patronymic)}
    </span>
  )
}

